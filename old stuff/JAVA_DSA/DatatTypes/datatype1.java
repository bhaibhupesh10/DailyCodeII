package DatatTypes;
public class datatype1 {
    public static void main(String[] args) {
        int a = 45;
        int b = 045;
        int c = 0x45;
        int d = 0b00110010;
        int e = 0x55;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}

